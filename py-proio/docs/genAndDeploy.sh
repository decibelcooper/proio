#!/bin/bash

git clone https://git@${PY_DOCS_REPO_REF} html

rm -rf html/*

make html

if [ $? -ne 0 ]; then
    exit $?
fi

touch html/.nojekyll

cd html

git add --all
git commit -m "Autogenerated via decibelcooper/proio Travis CI build: ${TRAVIS_BUILD_NUMBER}" -m "Commit: ${TRAVIS_COMMIT}"
git push --force "https://${PY_DOCS_REPO_TOKEN}@${PY_DOCS_REPO_REF}"

exit $?
